import { existsSync } from 'node:fs';
import { writeFile } from 'node:fs/promises';
import { join, resolve } from 'node:path';

export const REEL_AUDIO_PRESETS = Object.freeze(['calm-piano']);

const CALM_PIANO_TITLE = 'Calm Piano Bed';
const CALM_PIANO_LICENSE = 'Original generated audio created by this automation. Free to use as background music for this Instagram account; do not redistribute as a standalone music file.';

export function getConfiguredReelAudio(argv = {}, env = process.env) {
  const input = argv['reel-audio'] || argv.audio || env.REEL_AUDIO_PATH || env.REEL_AUDIO_URL || '';
  if (input) {
    const license = argv['reel-audio-license'] || env.REEL_AUDIO_LICENSE || '';
    if (!license) {
      throw new Error('REEL_AUDIO_LICENSE or --reel-audio-license is required when adding Reel audio.');
    }

    const title = argv['reel-audio-title'] || env.REEL_AUDIO_TITLE || null;
    const creator = argv['reel-audio-creator'] || env.REEL_AUDIO_CREATOR || null;
    const sourceUrl = argv['reel-audio-source-url'] || env.REEL_AUDIO_SOURCE_URL || (/^https?:\/\//i.test(input) ? input : null);
    if (!title && !sourceUrl) {
      throw new Error('REEL_AUDIO_TITLE or REEL_AUDIO_SOURCE_URL is required when adding Reel audio.');
    }

    return {
      input,
      preset: null,
      volume: clampAudioVolume(argv['reel-audio-volume'] || env.REEL_AUDIO_VOLUME || 0.18),
      attribution: {
        source: 'user_provided_audio',
        title,
        creator,
        sourceUrl,
        license,
        creditLine: argv['reel-audio-credit'] || env.REEL_AUDIO_CREDIT || buildAudioCreditLine({ title, creator }),
      },
    };
  }

  const preset = normalizePreset(argv['reel-audio-preset'] || env.REEL_AUDIO_PRESET || '');
  if (!preset) return null;
  if (!REEL_AUDIO_PRESETS.includes(preset)) {
    throw new Error(`Unsupported REEL_AUDIO_PRESET: ${preset}. Use ${REEL_AUDIO_PRESETS.join(', ')} or none.`);
  }

  return {
    input: null,
    preset,
    volume: clampAudioVolume(argv['reel-audio-volume'] || env.REEL_AUDIO_VOLUME || 0.14),
    attribution: {
      source: 'generated_audio_preset',
      title: CALM_PIANO_TITLE,
      creator: 'Instagram automation',
      sourceUrl: null,
      license: CALM_PIANO_LICENSE,
      creditLine: `음악: ${CALM_PIANO_TITLE} - generated by automation`,
    },
  };
}

export async function prepareReelAudioInput(reelAudio, {
  workDir,
  basename = 'reel-audio',
  root = process.cwd(),
  durationSec = 90,
} = {}) {
  if (!reelAudio) return null;
  if (reelAudio.preset === 'calm-piano') {
    const outputPath = join(workDir, `${basename}-calm-piano.wav`);
    await writeGeneratedCalmPiano(outputPath, { durationSec });
    return outputPath;
  }

  const source = reelAudio.input;
  if (/^https?:\/\//i.test(source)) {
    const url = new URL(source);
    const extension = mediaExtension(url.pathname) || 'mp3';
    const outputPath = join(workDir, `${basename}.${extension}`);
    const response = await fetch(source);
    if (!response.ok) throw new Error(`Failed to download Reel audio: ${response.status}`);
    await writeFile(outputPath, Buffer.from(await response.arrayBuffer()));
    return outputPath;
  }

  const localPath = resolve(root, source);
  if (!existsSync(localPath)) throw new Error(`Reel audio file does not exist: ${localPath}`);
  return localPath;
}

function normalizePreset(value) {
  const normalized = String(value || '').trim().toLowerCase();
  if (!normalized || ['none', 'off', 'false', '0'].includes(normalized)) return '';
  return normalized;
}

function buildAudioCreditLine({ title, creator }) {
  if (!title && !creator) return null;
  return `음악: ${[title, creator].filter(Boolean).join(' - ')}`;
}

function clampAudioVolume(value) {
  const number = Number(value);
  if (!Number.isFinite(number)) return 0.18;
  return Math.min(Math.max(number, 0.01), 1);
}

function mediaExtension(value) {
  const match = String(value || '').match(/\.([a-z0-9]{2,5})$/i);
  return match?.[1]?.toLowerCase() || null;
}

async function writeGeneratedCalmPiano(outputPath, { durationSec }) {
  const sampleRate = 44100;
  const seconds = Math.max(1, Math.min(Number(durationSec) || 90, 180));
  const totalSamples = Math.ceil(sampleRate * seconds);
  const samples = new Float32Array(totalSamples);
  const bpm = 68;
  const beat = 60 / bpm;
  const chords = [
    [261.63, 329.63, 392.00, 493.88],
    [196.00, 293.66, 392.00, 493.88],
    [220.00, 261.63, 329.63, 440.00],
    [174.61, 261.63, 349.23, 440.00],
    [146.83, 220.00, 293.66, 349.23],
    [196.00, 246.94, 293.66, 392.00],
    [164.81, 246.94, 329.63, 392.00],
    [174.61, 220.00, 261.63, 349.23],
  ];

  let eventIndex = 0;
  for (let t = 0; t < seconds + 2; t += beat / 2) {
    const chord = chords[Math.floor((t / (beat * 4)) % chords.length)];
    const frequency = chord[eventIndex % chord.length] * (eventIndex % 8 === 0 ? 0.5 : 1);
    addPianoNote(samples, sampleRate, t, frequency, 2.9, eventIndex % 8 === 0 ? 0.17 : 0.11);
    eventIndex += 1;
  }

  applyEcho(samples, sampleRate, 0.33, 0.22);
  applyEcho(samples, sampleRate, 0.71, 0.11);
  normalize(samples, 0.82);
  await writeFile(outputPath, encodeWav(samples, sampleRate));
}

function addPianoNote(samples, sampleRate, startSec, frequency, lengthSec, gain) {
  const start = Math.max(0, Math.floor(startSec * sampleRate));
  const length = Math.floor(lengthSec * sampleRate);
  const harmonics = [
    [1, 1],
    [2, 0.42],
    [3, 0.18],
    [4, 0.08],
  ];

  for (let i = 0; i < length && start + i < samples.length; i += 1) {
    const t = i / sampleRate;
    const attack = Math.min(1, t / 0.018);
    const decay = Math.exp(-2.65 * t);
    const envelope = attack * decay;
    let tone = 0;
    for (const [multiple, amount] of harmonics) {
      tone += Math.sin(2 * Math.PI * frequency * multiple * t) * amount;
    }
    samples[start + i] += tone * envelope * gain;
  }
}

function applyEcho(samples, sampleRate, delaySec, amount) {
  const delay = Math.floor(sampleRate * delaySec);
  for (let i = delay; i < samples.length; i += 1) {
    samples[i] += samples[i - delay] * amount;
  }
}

function normalize(samples, peakTarget) {
  let peak = 0;
  for (const sample of samples) peak = Math.max(peak, Math.abs(sample));
  if (!peak) return;
  const scale = peakTarget / peak;
  for (let i = 0; i < samples.length; i += 1) samples[i] *= scale;
}

function encodeWav(samples, sampleRate) {
  const channels = 1;
  const bytesPerSample = 2;
  const blockAlign = channels * bytesPerSample;
  const dataSize = samples.length * bytesPerSample;
  const buffer = Buffer.alloc(44 + dataSize);
  buffer.write('RIFF', 0);
  buffer.writeUInt32LE(36 + dataSize, 4);
  buffer.write('WAVE', 8);
  buffer.write('fmt ', 12);
  buffer.writeUInt32LE(16, 16);
  buffer.writeUInt16LE(1, 20);
  buffer.writeUInt16LE(channels, 22);
  buffer.writeUInt32LE(sampleRate, 24);
  buffer.writeUInt32LE(sampleRate * blockAlign, 28);
  buffer.writeUInt16LE(blockAlign, 32);
  buffer.writeUInt16LE(bytesPerSample * 8, 34);
  buffer.write('data', 36);
  buffer.writeUInt32LE(dataSize, 40);

  for (let i = 0; i < samples.length; i += 1) {
    const value = Math.max(-1, Math.min(1, samples[i]));
    buffer.writeInt16LE(Math.round(value * 32767), 44 + i * 2);
  }
  return buffer;
}
